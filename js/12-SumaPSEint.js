/* Realizar una suma de dos números:  Crear un algoritmo que permita al 
usuario ingresar dos números, realizar la suma de ambos y mostrar el resultado. */
let numOne = parseInt(prompt("Ingrese un numero:"))
let numTwo = parseInt(prompt("Ingrese otro numero:"))
document.writeln("La suma de los numero es: ", numOne + numTwo)